SET NAMES utf8mb4;
SET time_zone = '+08:00';
USE `movie`;

START TRANSACTION;

-- 将演示长评统一补充为更适合详情页展示的长正文，保留 Markdown 格式和图片语法。
UPDATE `long_reviews`
SET content_md = CONCAT(content_md, '

### 四、适合继续讨论的角度

这篇长评仍然可以继续延展。真正好的电影讨论不只给出“喜欢”或“不喜欢”的判断，还应该把观影感受拆成更具体的层次：人物为什么这样选择，镜头为什么停在这个位置，音乐为什么在这一刻进入，结尾为什么会留下余味。把这些问题写清楚，评论就不再只是情绪记录，而会变成其他用户继续交流的起点。

- 可以继续分析影片中的关键场景，观察人物关系如何在细节里变化。
- 可以把短评中的即时感受扩展成长评中的完整论证。
- 可以结合导演风格、时代背景和同类型影片进行横向比较。
- 可以在回复区提出不同理解，让长评从单篇文章变成讨论主题。

> 长评的价值不在于一定给出标准答案，而在于把一次观影经验整理成可以被别人阅读、回应和再思考的文本。

因此，这类内容适合在电影详情页、首页优质长评栏目和个人主页中重点展示。它既能补充电影基础资料，也能让用户感受到社区的真实活跃度。')
WHERE id BETWEEN 101 AND 112
  AND content_md NOT LIKE '%### 四、适合继续讨论的角度%';

-- 将演示资讯统一补充为更完整的资讯详情页正文。
UPDATE `news_articles`
SET content = CONCAT(content, '

从产品展示角度看，这类资讯不应该只停留在标题和摘要层面。完整资讯正文可以承担三个作用：第一，为用户提供进入电影详情页的理由；第二，为长评、短评和解读视频提供背景线索；第三，为首页推荐位提供可运营的内容素材。用户点击资讯后，如果只能看到几行简介，很难形成继续浏览的动机；如果资讯正文足够完整，就可以自然引导用户查看关联电影、阅读长评、参与讨论或收藏内容。

在后台管理中，资讯还可以与电影、演员、导演等对象建立关联。管理员审核通过后，可以根据浏览量、点赞数、评论数或人工运营需要，把资讯设置为首页轮播、热门资讯或电影详情页关联内容。电影官方用户投稿的资讯，也可以通过统一审核流程进入发布队列，审核通过后再对外展示。这样既能保证内容来源更丰富，也能避免未经审核的信息直接影响平台质量。

后续前后端联调时，资讯详情页需要重点检查正文排版、封面图展示、关联电影跳转、发布时间、浏览量和移动端阅读体验。对于长正文，前端应保证段落间距舒适、行宽不过长、图片加载稳定，并在黑金主题下保持文字对比度。这样资讯模块才不只是首页的装饰栏目，而能真正成为用户理解电影动态和进入深度内容的入口。')
WHERE id BETWEEN 201 AND 212
  AND content NOT LIKE '%从产品展示角度看，这类资讯不应该只停留在标题和摘要层面%';

COMMIT;
